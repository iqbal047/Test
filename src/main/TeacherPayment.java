    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if (tTable.getRowCount() > 0) {
            for (int i = tTable.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }

        Connection con = null;
        CallableStatement cstmt = null;
        ResultSet rst = null;
        try {
            con = DatabaseConnector.getConnection();
            cstmt = con.prepareCall("{CALL getAllTeacherPayment()}");
            cstmt.execute();

            rst = cstmt.getResultSet();
            while (rst.next()) {
                tPid = rst.getString(1);
                tTid = rst.getString(2);
                tTname = rst.getString(3);
                tTmonth = rst.getString(4);
                tTamount = rst.getString(5);
                tTdate = rst.getString(6);

                model.addRow(new Object[]{tPid, tTid, tTname, tTmonth, tTamount, tTdate});
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                cstmt.close();
                rst.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        DataController.loadTeachersId(tId);
        DataController.loadTeachersPaymentId(tTpid);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tIdItemStateChanged
        if (tId.getSelectedIndex() > 0) {
            Connection con = null;
            CallableStatement cstmt = null;
            ResultSet rst = null;
            try {
                con = DatabaseConnector.getConnection();
                cstmt = con.prepareCall("{CALL getTeacherInfo(?)}");
                cstmt.setString(1, tId.getSelectedItem().toString());
                cstmt.execute();
                rst = cstmt.getResultSet();
                while (rst.next()) {
                    tName.setText(rst.getString("name"));
                }

                cstmt = con.prepareCall("{CALL sumPayAmount(?)}");
                cstmt.setString(1, tId.getSelectedItem().toString());
                cstmt.execute();
                rst = cstmt.getResultSet();
                if (rst.next()) {
                    tAmount.setText(rst.getString(1));
                } else {
                    tAmount.setText("");
                }

                cstmt = con.prepareCall("{CALL sumPayMonth(?)}");
                cstmt.setString(1, tId.getSelectedItem().toString());
                cstmt.execute();
                rst = cstmt.getResultSet();
                if (rst.next()) {
                    tMonth.setText(rst.getString(1));
                } else {
                    tMonth.setText("");
                }

                cstmt = con.prepareCall("{CALL getTeacherPayInfo(?)}");
                cstmt.setString(1, tId.getSelectedItem().toString());
                cstmt.execute();
                rst = cstmt.getResultSet();
                if (rst.next()) {
                    tDate.setText(rst.getString("p_date"));
                } else {
                    tDate.setText("");
                }
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    con.close();
                    cstmt.close();
                    rst.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } else {
            tName.setText("");
            tMonth.setText("");
            tAmount.setText("");
            tDate.setText("");
        }
    }//GEN-LAST:event_tIdItemStateChanged

