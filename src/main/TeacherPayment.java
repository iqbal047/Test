    private void saveStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveStudentActionPerformed
        if (tTable.getRowCount() > 0) {
            for (int i = tTable.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        getPaymentValue();
        if (tId.getSelectedIndex() > 0 && !vName.isEmpty() && !vMonth.isEmpty() && !vAmount.isEmpty() && !vDate.isEmpty()) {
            DataController.saveTeacherPayment(vId, vName, vMonth, vAmount, vDate);
            JOptionPane.showMessageDialog(null, "Payment Saved Successfully! Please Generate Receipt...");
            DataController.loadTeachersId(tId);
        } else {
            JOptionPane.showMessageDialog(null, "Please give all information correctly!");
        }
        DataController.loadTeachersId(tId);
        DataController.loadTeachersPaymentId(tTpid);
        
    }//GEN-LAST:event_saveStudentActionPerformed

