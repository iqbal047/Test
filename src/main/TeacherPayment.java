    private void tTpidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tTpidItemStateChanged
        if (tTpid.getSelectedIndex() > 0) {
            Connection con = null;
            PreparedStatement cstmt = null;
            ResultSet rst = null;
            try {
                con = DatabaseConnector.getConnection();
                cstmt = con.prepareStatement("select * from teacher_payment where tp_id=?");
                cstmt.setString(1, tTpid.getSelectedItem().toString());
                cstmt.execute();
                rst = cstmt.getResultSet();
                while (rst.next()) {
                    tId.setSelectedItem(rst.getString("t_id"));
                }
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    con.close();
                    cstmt.close();
                    rst.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } 
    }//GEN-LAST:event_tTpidItemStateChanged

